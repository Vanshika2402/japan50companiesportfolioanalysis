#Risk-return metrics

trading_days = 252
risk_free_rate = 0.01

metrics = pd.DataFrame(index=returns.columns)

metrics["Annual Return"] = returns.mean() * trading_days
metrics["Annual Volatility"] = returns.std() * np.sqrt(trading_days)
metrics["Sharpe Ratio"] = (metrics["Annual Return"] - risk_free_rate) / metrics["Annual Volatility"]


# Extract scalar value from benchmark_variance
benchmark_variance_scalar = benchmark_returns.var().item()

betas = []
for ticker in returns.columns:

    # Convert benchmark_returns to a 1D Series for compatibility with np.cov
    covariance = np.cov(returns[ticker], benchmark_returns.squeeze())[0][1]
    beta = covariance / benchmark_variance_scalar
    betas.append(beta)

metrics["Beta"] = betas

# Downside risk

downside_returns = returns.copy()
downside_returns[downside_returns > 0] = 0
metrics["Downside Risk"] = downside_returns.std() * np.sqrt(trading_days)

# Maximum drawdown

