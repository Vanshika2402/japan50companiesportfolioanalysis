# Portfolio functions

mean_returns = returns.mean() * trading_days
cov_matrix = returns.cov() * trading_days
n = len(returns.columns)

def portfolio_performance(weights):
    portfolio_return = np.sum(mean_returns * weights)
    portfolio_volatility = np.sqrt(weights.T @ cov_matrix @ weights)
    sharpe_ratio = (portfolio_return - risk_free_rate) / portfolio_volatility
    return portfolio_return, portfolio_volatility, sharpe_ratio

def negative_sharpe(weights):
    return -portfolio_performance(weights)[2]

def portfolio_volatility(weights):
    return portfolio_performance(weights)[1]

constraints = {"type": "eq", "fun": lambda weights: np.sum(weights) - 1}
bounds = tuple((0, 0.10) for _ in range(n))
initial_weights = np.array([1 / n] * n)

# Equal-weighted portfolio

equal_weights = np.array([1 / n] * n)

eq_return, eq_volatility, eq_sharpe = portfolio_performance(equal_weights)

print("Equal-Weighted Portfolio")
print("Return:", eq_return)
print("Volatility:", eq_volatility)
print("Sharpe:", eq_sharpe)

# Minimum variance portfolio

min_var_result = minimize(
    portfolio_volatility,
    initial_weights,
    method="SLSQP",
    bounds=bounds,
    constraints=constraints
)

min_var_weights = min_var_result.x
min_return, min_volatility, min_sharpe = portfolio_performance(min_var_weights)

print("Minimum Variance Portfolio")
print("Return:", min_return)
print("Volatility:", min_volatility)
print("Sharpe:", min_sharpe)

# Maximum Sharpe ratio portfolio

max_sharpe_result = minimize(
    negative_sharpe,
    initial_weights,
    method="SLSQP",
    bounds=bounds,
    constraints=constraints
)

max_sharpe_weights = max_sharpe_result.x
max_return, max_volatility, max_sharpe = portfolio_performance(max_sharpe_weights)

print("Maximum Sharpe Portfolio")
print("Return:", max_return)
print("Volatility:", max_volatility)
print("Sharpe:", max_sharpe)

